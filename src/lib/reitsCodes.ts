export function getSecId(code: string, exchange: 'sz' | 'sh'): string {
  return exchange === 'sz' ? `0.${code}` : `1.${code}`;
}

export function getReitsCode(secId: string): string {
  if (secId.startsWith('0.')) {
    return secId.substring(2);
  } else if (secId.startsWith('1.')) {
    return secId.substring(2);
  }
  return secId;
}
